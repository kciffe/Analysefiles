# 统一的五维评估规则
_EVALUATION_RUBRIC = dedent("""
【固定评估维度】
除回答用户需求外，最终报告必须对以下 5 个维度进行分析，并给出等级、分值、依据和证据来源。

1. 路线可行性
定义：
根据待分析技术方案描述是否完整、实现条件是否明确、所需资源（如数据、算力、软件、硬件、人才、工程环境等）是否可满足、
是否存在足够的相关研究/工程案例支撑，综合判断该技术路线是否具有可实施性和可复现性。

评分规则：
- 3 分（可行性高）：同时满足多数条件：方案描述较完整；实现路径较清晰；所需资源基本可满足；有较充分相关技术或案例支撑；具备较好的复现可能。
- 2 分（可行性一般）：介于高与低之间，存在部分实现条件不明确、资源不完全清晰、支撑证据一般等情况。
- 1 分（可行性低）：出现以下情况之一：方案描述模糊；关键实现条件不清楚；资源明显不足；相关支撑技术/案例过少；可复现性较弱。

2. 先进性
定义：
根据待分析技术在其所属技术领域中的性能、效果、效率、成本、泛化能力或其他关键指标，与同类主流技术或当前较优方案进行对比，综合判断其先进性。

评分规则：
- 3 分（先进性较高）：若存在可比量化指标，则达到当前同类较优水平或与最优差距很小（可参考 ≤ 2%，具体按领域动态调整）；若无统一指标，则证据表明其明显优于主流可比方案。
- 2 分（先进性一般）：若存在可比量化指标，则低于当前较优水平但差距不大（可参考 2%～10%，具体按领域动态调整）；若无统一指标，则仅达到主流水平或略优/略弱。
- 1 分（先进性偏低）：若存在可比量化指标，则明显低于当前较优水平（可参考 > 10%，具体按领域动态调整）；若无统一指标，且没有足够证据说明其优于可比方案。

3. 成熟度
定义：
根据待分析技术是否已有应用描述、原型系统、真实场景验证、工程实现、开源实现、产品化案例或稳定实验验证，综合判断其成熟度。

评分规则：
- 3 分（成熟度高）：存在明确应用性描述，且已有具体场景、系统、产品或稳定工程验证支撑。
- 2 分（成熟度一般）：存在一定应用性描述或实验验证，但落地场景不充分、工程化程度一般。
- 1 分（成熟度低）：仅有概念性描述，缺少实际应用或稳定验证，或文档中明确指出应用限制较多。

4. 创新性
定义：
根据待分析技术方案在技术思路、关键机制、模块设计、系统组合方式等方面，是否明显区别于现有主流方案，综合判断其创新性。

评分规则：
- 3 分（创新性较好）：采用了明显不同于主流方案的技术路径或关键机制，且先进性达到较高水平。
- 2 分（创新性一般）：存在一定新意或组合创新，但与现有路线差异有限，或虽有差异但效果优势一般。
- 1 分（创新性较差）：与已有主流方案高度相似，主要是常规组合、微调或替换，缺少明确创新点。

5. 自主性（技术可控性 / 外部依赖度）
定义：
根据待分析技术对外部闭源模型、国外软硬件、专有平台、第三方 API、受限数据源、不可控供应链等依赖程度，综合判断其自主可控程度。

评分规则：
- 3 分（自主性高）：核心所需资源基本可在自有或可控环境中满足，外部依赖较弱，替代性较强。
- 2 分（自主性一般）：存在一定外部依赖，但不构成核心卡点，仍有替代或缓解空间。
- 1 分（自主性低）：关键资源中有两项及以上强依赖外部支持，或依赖闭源/不可控组件明显。

【统一要求】
- 所有判断都必须优先基于证据，不得编造。
- 若某维度证据不足，必须明确写“证据不足”，不能强行下结论。
- 所有评分都要写清楚“判断依据 + 对应证据来源”。
- 自主性在本项目中按“技术可控性 / 外部依赖度”理解，不是机械照搬原始军工语境。
""").strip()


# 生成检索证据的计划，只产出“读什么、为什么读”
_GENERATE_EVIDENCE_PROMPT = dedent("""
你是“需求证据分析助手”。

输入:
1) 用户需求：{requirement}
2) 候选文档：{candidate_documents}

candidate_documents 中每篇文档包含：
- id: int
- title: str
- abstract: str | None
- keywords: list[str]
- structure_info: dict | None
- structure_info.sections 为章节树，节点含 title / level / children

{evaluation_rubric}

任务：
- 你需要为最终技术分析报告挑选最值得阅读的章节。
- 最终报告除了回答用户需求之外，还必须覆盖“五个固定评估维度”。
- 仅根据用户需求、文档摘要、关键词和文档结构，分析“为什么要读某些章节”，并写出理由。
- 不要编造章节内容。
- 优先选择能直接支撑五个维度判断的章节，并尽量保证五个维度都有证据覆盖。
- 若无法覆盖全部维度，优先覆盖与用户需求最相关、且最有证据支撑的维度。

章节优先级参考：
- 路线可行性：Method / System / Framework / Pipeline / Resource / Implementation / Discussion / Limitation
- 先进性：Experiment / Results / Benchmark / Comparison / Evaluation / SOTA
- 成熟度：Application / Case Study / Deployment / Real-world / User Study / Conclusion
- 创新性：Introduction / Contribution / Related Work / Method / Ablation
- 自主性：Implementation / Resource / Dependency / Platform / License / Open-source / Limitation / Discussion

筛选规则：
- 优先方法、实验、结果、消融、讨论、结论、案例、部署、限制等高信息密度章节。
- 若某文档无 structure_info 或无相关章节，可跳过。
- 若整体无可读章节，输出 []。
- 若多个章节作用相近，优先选择最能直接支持“评分判断”的章节。
- why_read 不能泛泛而谈，必须明确指出该章节主要支撑哪个维度，以及准备解决什么判断问题。

输出要求（严格）：
- 直接输出 JSON 数组，不要 markdown，不要额外的中英文解释。
- 每个元素必须且仅包含：
  - doc_id: int
  - section_title: string
  - why_read: string
- section_title 必须是 candidate_documents 中 structure_info 结构里的原始章节标题，必须严格与原文一致，包括符号和空格。

why_read 写法要求：
- 必须显式写出对应维度，建议格式：
  "[先进性][创新性] 该章节可能提供与主流方案的对比、核心机制与贡献边界，可用于判断先进性和创新性。"
- 若同一章节可同时支持多个维度，可以写多个维度标签。

示例：
[
  {{"doc_id": 1, "section_title": "4.2 Experiments", "why_read": "[先进性][成熟度] 该章节可能提供性能对比、实验验证强度和应用效果证据，可用于判断先进性和成熟度。"}},
  {{"doc_id": 1, "section_title": "4.4 Ablation Study", "why_read": "[创新性] 该章节可能说明关键模块贡献与区别于已有方案的机制，可用于判断创新性。"}}
]
""").strip()


